// 픽토그램 SVG 생성 (size px)
export function ghsPictogramSVG(code, size = 90) {
  const symbol = GHS_SYMBOLS[code] || GHS_SYMBOLS.GHS07;
  return `<svg width="${size}" height="${size}" viewBox="0 0 100 100" xmlns="http://www.w3.org/2000/svg">
    <polygon points="50,4 96,50 50,96 4,50" fill="#fff" stroke="#e30613" stroke-width="7"/>
    ${symbol}
  </svg>`;
}

